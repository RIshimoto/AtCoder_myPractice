#include <bits/stdc++.h>
using namespace std;

int main(void)
{
	char x; cin >> x;

	switch (x)
	{
		case 'B':
			cout << 2 << endl;
			return 0;
		case 'C':
			cout << 3 << endl;
			return 0;
		case 'D':
			cout << 4 << endl;
			return 0;
		case 'E':
			cout << 5 << endl;
			return 0;
		default:
			cout << 1 << endl;
			return 0;
	}
}
